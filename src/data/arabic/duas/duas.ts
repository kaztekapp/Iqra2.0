// Get all duas
export const getAllDuas = (): Dua[] => {
  return DUAS;
};

// Get dua by ID
export const getDuaById = (duaId: string): Dua | undefined => {
  return DUAS.find((dua) => dua.id === duaId);
};

// Get duas by category
export const getDuasByCategory = (category: DuaCategory): Dua[] => {
  return DUAS.filter((dua) => dua.category === category);
};

// Get all categories that have duas
export const getAvailableCategories = (): DuaCategory[] => {
  const categories = new Set(DUAS.map((dua) => dua.category));
  return Array.from(categories);
};
